#include "0000. Typedef.h"
/*********************************************************************************
  *函数： Print
  *描述： 打印顺序栈的所有元素
		SqStack stack		指向待打印顺序表的指针
  *输出： 打印顺序栈的所有元素
  *返回：	无
**********************************************************************************/
void Print(SqStack stack) {
	for (int i = 0; i <= stack.top; i++) {			/* 遍历到顺序栈顶 */
		printf("%d ", stack.Date[i]);			/* 打印当前数据 */
		// i的 "终止位置" stack.top	在顺序栈的栈顶
	}
	printf("\n");
}