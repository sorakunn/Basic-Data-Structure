/*********************************************************************************
  *Function:  		CreateTail
  *Description：		从前往后正向建立单链表，每次将新元素插在"表尾"
					SLNode *head	待执行 "尾插法" 建表的头指针
					Elem x[]		存放待插入元素的数组
					int n			数组中待插入元素的个数
  *Output:  完成元素插入的单链表的头指针
  *Return:  无
**********************************************************************************/
void CreateTail(SLNode *head, Elem x[], int n) {
	SLNode *p = head;
	for (int i = 0; i < n; i++) {							// 对于数组中的每个数据
		SLNode *temp = (SLNode*)malloc(sizeof(SLNode));		// 建立一个结点
		temp->Date = x[i];									// 放入数据
		temp->next = NULL;									/* 新结点的尾放入 "NULL" */
		p->next = temp;										/* 新结点的头放在 "表尾" */
		p = temp;											/* 新结点 "变成表尾" */
	}
}